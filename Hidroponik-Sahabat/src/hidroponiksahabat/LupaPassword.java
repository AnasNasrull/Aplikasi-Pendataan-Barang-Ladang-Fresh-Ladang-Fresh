    private void ButtonPilihActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonPilihActionPerformed
        quest = ComboTanya.getSelectedItem().toString();
        try {
            Statement stat = (Statement) koneksi.koneksiDB().createStatement();
            String sql = "Select * from reset where jenis='" + quest + "'";
            ResultSet res = stat.executeQuery(sql);

            while (res.next()) {
                tanya = res.getString("question");
                jawab = res.getString("answer");

                pertanyaan.setText(tanya);
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
    }//GEN-LAST:event_ButtonPilihActionPerformed

    private void ButtonGantiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonGantiActionPerformed
        if (passwordL.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Kolom Password Tidak Boleh Kosong!", "Kesalahan", JOptionPane.ERROR_MESSAGE);
        } else {
            if (jawab == null ? jawaban.getText() == null : jawab.equals(jawaban.getText())) {
                Ganti();
            } else {
                JOptionPane.showMessageDialog(null, "Jawaban Anda Salah!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_ButtonGantiActionPerformed

