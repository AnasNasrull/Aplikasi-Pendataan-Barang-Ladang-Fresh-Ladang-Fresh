    private void tambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tambahActionPerformed
           Object[] obj = new Object[5];
           
           barang = jComboBox1.getSelectedItem().toString();
           jumlah = Integer.parseInt(jumlahBarang.getText());
           int stok_barang = 0;
           try {
            Statement stat = (Statement) koneksi.koneksiDB().createStatement();
            String sql = "Select * from barang where nama_barang = '" + barang + "'";
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                id = res.getString("id_barang");
                harga = Integer.parseInt(res.getString("harga_barang"));
                stok_barang = res.getInt("jumlah_barang");
            }
        } catch (SQLException err) {
            JOptionPane.showMessageDialog(null, err.getMessage());
        }
        if (jumlah > stok_barang){
            JOptionPane.showMessageDialog(null, "Stok tidak cukup, Stok hanya tersedia "+stok_barang, "Peringatan", JOptionPane.WARNING_MESSAGE);
            
        }
        else{
            update.put(id, stok_barang);
           total = jumlah*harga;
        obj[0] = id;
        obj[1] = barang;
        obj[2] = jumlah;
        obj[3] = harga;
        obj[4] = total;
        
        model.addRow(obj);
        jumlah_transaksi += total;
        total_transaksi.setText(jumlah_transaksi.toString());
        }
       
    }//GEN-LAST:event_tambahActionPerformed

    private void hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hapusActionPerformed
        int ok = 1;
        DefaultTableModel model = (DefaultTableModel) tableTambah.getModel();
        int row = tableTambah.getSelectedRow();
        id = model.getValueAt(row, 0).toString();
        if(row>=0){
             ok=JOptionPane.showConfirmDialog(null, "Yakin Mau Hapus?","Konfirmasi",JOptionPane.YES_NO_OPTION);
        }
        if (ok == 0){
            model.removeRow(row);
            update.remove(id);
        }
    }//GEN-LAST:event_hapusActionPerformed

    private void selesaiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_selesaiActionPerformed
        int jumlah = tableTambah.getRowCount();
        
        if (jumlah > 0) {
            try {
                Statement stat = (Statement) koneksi.koneksiDB().createStatement();
                String sql = "insert into transaksi(id_transaksi, jumlah_transaksi) values ("+"'"+ idTransaksi +"',"+ jumlah_transaksi +")";
                stat.execute(sql);
                for(int i=0; i<tableTambah.getRowCount(); i++){
                    id = tableTambah.getValueAt(i, 0).toString();
                    jumlah = Integer.parseInt(tableTambah.getValueAt(i, 2).toString());
                    sql = "insert into item_transaksi(id_transaksi, id_barang, jumlah_barang) values (" + "'" + idTransaksi + "'," + "'" + id + "'," + jumlah + ")";
                    stat.execute(sql);
                    updatebarang(id, jumlah);
                    }
                                
            } catch (SQLException err) {
                JOptionPane.showMessageDialog(null, err.getMessage());
            }
            JOptionPane.showMessageDialog(null, "Data berhasil disimpan","Berhasil", JOptionPane.INFORMATION_MESSAGE);
            this.setVisible(false);
            new Home().setVisible(true);
          } else {
            JOptionPane.showMessageDialog(null, "Data Masih Kosong!!!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_selesaiActionPerformed

